/**
 * Selecciona N fotos al azar del pool sin repetir.
 */
export function getRandomQuestions(count = 15) {
  const shuffled = [...photoPool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
